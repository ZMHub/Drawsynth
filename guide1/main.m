%%%%%% Private functions %%%%%%
function execSynth(Pitch)
global Pitches PitchFreqs;
fs = 44100;
for ii = 1:length(Pitches)
    if(Pitch == Pitches{ii})        
        [out_data, fs] = synth(PitchFreqs(ii),@getwaveform, @getenvelop, fs);
    end
end
sound(out_data,fs);

